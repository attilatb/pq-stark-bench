// Renders docs/TECHNICAL-NOTE.md to HTML at build time and writes it as a TS
// module the site imports. Kept a build step (not a runtime dependency) so the
// bundle stays small and the note stays in sync with the repo.
//
// typographer is deliberately OFF so markdown-it never rewrites "--" into an em
// dash, which would violate the project's hyphens-only house rule.

import { readFileSync, writeFileSync, mkdirSync, existsSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";
import MarkdownIt from "markdown-it";

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = join(here, "..", "..");
const notePath = join(repoRoot, "docs", "TECHNICAL-NOTE.md");
const outDir = join(here, "..", "src", "generated");
const outFile = join(outDir, "note.ts");

if (!existsSync(notePath)) {
  throw new Error(`Technical note not found at ${notePath}`);
}

const markdown = readFileSync(notePath, "utf8");

const md = new MarkdownIt({
  html: false, // the note authors no raw HTML; keep it that way
  linkify: true,
  typographer: false, // never rewrite -- into an em dash
  breaks: false,
});

// Open external links in a new tab, safely.
const defaultRender =
  md.renderer.rules.link_open ||
  ((tokens, idx, options, _env, self) => self.renderToken(tokens, idx, options));
md.renderer.rules.link_open = (tokens, idx, options, env, self) => {
  const href = tokens[idx].attrGet("href") || "";
  if (/^https?:\/\//.test(href)) {
    tokens[idx].attrSet("target", "_blank");
    tokens[idx].attrSet("rel", "noopener noreferrer");
  }
  return defaultRender(tokens, idx, options, env, self);
};

const html = md.render(markdown);

// Guard: the rendered HTML must not contain an em dash either.
if (html.includes("—")) {
  throw new Error("Rendered note contains an em dash. House style is hyphens only.");
}

mkdirSync(outDir, { recursive: true });
writeFileSync(
  outFile,
  `// Generated by scripts/build-note.mjs from docs/TECHNICAL-NOTE.md. Do not edit.\n` +
    `export const noteHtml = ${JSON.stringify(html)};\n`
);

console.log(`build-note: rendered docs/TECHNICAL-NOTE.md -> src/generated/note.ts (${html.length} chars)`);
